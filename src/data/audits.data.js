export function seedAudits() {
  if (!canAutoSeed()) {
    if (!window.AUDITS_DATA) window.AUDITS_DATA = [];
    if (window.auditTab == null) window.auditTab = 0;
    if (window.auditSelected == null) window.auditSelected = null;
    if (!window.auditFilter) window.auditFilter = { type: '', statut: '', q: '' };
    return;
  }
  if (!window.AUDITS_DATA?.length) {
    window.AUDITS_DATA = AUDITS_SEED.map((a) => ({ ...a, actions: [...(a.actions || [])] }));
  }
  if (window.auditTab == null) window.auditTab = 0;
  if (window.auditSelected == null && window.AUDITS_DATA[0]) window.auditSelected = window.AUDITS_DATA[0].id;
  if (!window.auditFilter) window.auditFilter = { type: '', statut: '', q: '' };
}
