export function seedSstRisks() {
  if (!canAutoSeed()) {
    if (!window.sst_risks) window.sst_risks = [];
    if (window.sst_selectedId === undefined) window.sst_selectedId = null;
    if (window.sst_step == null) window.sst_step = 1;
    if (!window._sstFilter) window._sstFilter = { zone: '', niv: '', stat: '', q: '', gf: null };
    return;
  }
  if (!window.sst_risks?.length) {
    window.sst_risks = SST_RISKS_SEED.map((r) => ({ ...r }));
  }
  if (window.sst_selectedId === undefined) window.sst_selectedId = null;
  if (window.sst_step == null) window.sst_step = 1;
  if (!window._sstFilter) window._sstFilter = { zone: '', niv: '', stat: '', q: '', gf: null };
}
